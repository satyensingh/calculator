function addNumbers(num1, num2){
    return num1+num2;
}
function subNumbers(num1, num2){
    return num1-num2;
}
function mulNumbers(num1, num2){
    return num1*num2;
}
function divNumbers(num1, num2){
    return num1/num2;
}

exports.add = addNumbers;
exports.sub = subNumbers;
exports.mul = mulNumbers;
exports.div = divNumbers;
